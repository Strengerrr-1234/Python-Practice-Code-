# Operator              Description                   Example
#
#     is         Return True if both variables        x is y
#                    are the same object
#
#     is not     Return True if both variables        x is not y
#                  are not the same object

x = 10
y = 10
print(x is y)
print(x==y)

print(x is y, x==y)

print(x is not y, x!=y)