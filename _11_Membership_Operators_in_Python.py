# Operator                        Description                       Example
#
#    in              Return True if a sequence with the              x in y
#                 Specified value is present in the object
#
#    not in          Return True if a sequence with the             x not in y
#                 Specified value is not present in the object

string1 = "Hello"
print('H' in string1)

l = [10,20,30,40]
print(50 not in l)